def bubble_sorter(array)
    flag = true
    while flag
        flag = false
        array.each_with_index do |element, index|
            if index+1 < array.length
                if array[index] > array[index+1]
                    flag = true
                    array[index], array[index+1] = array[index+1], array[index]
                end
            end
        end
    end
    return array
end

